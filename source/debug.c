void ircsp_debug (char *format, ...)
{
	#if defined (__DEBUG__)
		FILE *f;
		char buf[256];
		va_list msg;

		va_start(msg, format);
		vsprintf(buf, format, msg);

		f = fopen(__DEBUG_FILE__, "a+");

		fprintf(f, "%s", buf);
		fflush(f);
		fclose(f);

		#if defined (__DEBUG_CONSOLE__)
			(void)printf("%s", buf);
		#endif // __DEBUG_CONSOLE__

	#endif // __DEBUG__
}

